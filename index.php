<p>
I am  a postdoc at the <a href="http://idl.cs.washington.edu">UW Interactive Data Lab</a> at the <a href="http://cs.washington.edu">University of Washington</a> Department of Computer Science, supervised by  <a href="http://homes.cs.washington.edu/~jheer">Jeff Heer</a>, where I work in the area of <b>information visualization</b>. I am specifically interested if we can use the perceptual system to make people into <i>natural statisticians</i>: that is, can we present data in such a way that the statistical content of the data can be examined and estimated visually, even by viewers without a strong statistical background? In places where human decision-making is subject to perceptual or cognitive biases, can we <i>de-bias</i> the visual presentation of data? 
</p>

<p>
 I received my PhD. from the <a href="http://cs.wisc.edu">University of Wisconsin-Madison</a> Department of Computer Sciences in August, 2015. As a PhD. student, I worked with  <a href="/~gleicher/">Michael Gleicher</a> in the <a href="http://graphics.cs.wisc.edu">UW Graphics Group</a>, where I worked in the area of  information visualization, especially in issues related to text analytics for the humanities.  In addition I was also involved with the <a href="http://dighum.wisc.edu/">Digital Humanities</a> working group.
</p>
	<p>
	My current <a href="./research.php">research interests</a> include the presentation of statistical data to non-experts, visualizations for the de-biasing of decision-making under uncertainty, and the digital humanities.
